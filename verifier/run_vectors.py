"""Run the HESO/1 conformance vectors against this clean-room reference verifier.

Two corpora are executed:

  1. The LEGACY web-observation goldens (``vectors/heso-1.0-vectors.json``):
       - every plat vector: assert ``plat_hash(input_json)`` == recorded hash.
       - the sealed-envelope vector: assert
         ``verify_sealed_plat(sealed_envelope_json)`` == expected outcome.
     (The old un-run ``receipt_vector`` was dropped in P1 — see the
     ``_dropped_receipt_vector`` note in that file. Its receipt coverage moved to
     the rust-reference ``action_receipt_v2`` corpus below.)

  2. The CROWN-JEWEL goldens (``vectors/heso-1.0-crown-vectors.json``):
       - ``action_receipt_v2``  : ``verify_action_receipt`` == expected outcome.
       - ``chain``              : ``verify_chain`` == expected outcome.
       - ``redaction``          : ``verify_reveal`` agrees with ``reveal_verifies``.
       - ``taxonomy_classify``  : clean-room ``classify`` == expected class/verb/
                                  primitive/effect (over the open ``taxonomy.toml``).
       - ``rfc6962``            : leaf/node/empty hashes + inclusion proof reproduce.
       - ``dsse_pae``           : ``dsse_pae`` reproduces ``pae_hex`` and
                                  ``verify_dsse`` accepts the golden envelope.

  3. The commitment WIRE-PAYLOAD goldens (``vectors/heso-1.0-wire-vectors.json``,
     P3 / ADR-0003):
       - ``commitment_wire``    : clean-room re-derivation of the FULL receipt ->
                                  commitment+indexes projection byte-matches the
                                  recorded ``wire_payload``; the source receipt
                                  verifies; and NO raw-content substring survives
                                  onto the wire.
       - ``redact_before_sign`` : the redacted source receipt verifies, the reveal
                                  recomputes (good salt yes / bad salt no), and
                                  neither the raw value nor the reveal salt is on
                                  the wire.
       - ``primitive_index``    : the five-spine destructive-primitive enum + each
                                  case's (primitive, decision) are in-spine.

Exit 0 iff every vector passes; exit 1 (printing a diff per failure) otherwise.

The legacy crypto constants in the vectors file are real (generated by the Rust
reference). If a value still holds the ``<GENERATED_AT_MERGE>`` placeholder it is
reported PENDING and the run exits non-zero — that path remains for safety but no
crown vector ships a placeholder.
"""

from __future__ import annotations

import json
import os
import sys

import heso_verify as hv

# The merge-time sentinel a vector field carries until the conformance job fills
# it from the Rust reference. Matched as a PREFIX so variants like
# "<GENERATED_AT_MERGE:foo>" are caught too.
PLACEHOLDER_PREFIX = "<GENERATED_AT_MERGE"

_VEC_DIR = os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))), "vectors")
LEGACY_PATH = os.path.join(_VEC_DIR, "heso-1.0-vectors.json")
CROWN_PATH = os.path.join(_VEC_DIR, "heso-1.0-crown-vectors.json")
WIRE_PATH = os.path.join(_VEC_DIR, "heso-1.0-wire-vectors.json")
TAXONOMY_PATH = os.path.join(os.path.dirname(_VEC_DIR), "taxonomy.toml")


def _is_placeholder(value) -> bool:
    return isinstance(value, str) and value.startswith(PLACEHOLDER_PREFIX)


def _contains_placeholder(obj) -> bool:
    if isinstance(obj, str):
        return _is_placeholder(obj)
    if isinstance(obj, dict):
        return any(_contains_placeholder(v) for v in obj.values())
    if isinstance(obj, list):
        return any(_contains_placeholder(v) for v in obj)
    return False


class Report:
    def __init__(self) -> None:
        self.passed = 0
        self.failures: list[str] = []
        self.pending: list[str] = []

    def ok(self) -> None:
        self.passed += 1

    def fail(self, label: str, expected, actual) -> None:
        self.failures.append(f"{label}\n    expected: {expected}\n    actual:   {actual}")

    def pend(self, label: str) -> None:
        self.pending.append(label)


# ── Legacy web-observation corpus ───────────────────────────────────────────
def run_legacy(rep: Report) -> None:
    with open(LEGACY_PATH, encoding="utf-8") as fh:
        vectors = json.load(fh)

    for vec in vectors.get("plat_vectors", []):
        vid = vec.get("id", "?")
        expected = vec.get("plat_hash")
        actual = hv.plat_hash(vec["input_json"])
        if _is_placeholder(expected):
            rep.pend(f"plat/{vid}: plat_hash awaiting reference (got {actual})")
            continue
        if actual != expected:
            rep.fail(f"plat/{vid}: plat_hash mismatch", expected, actual)
        else:
            rep.ok()

    if "sealed_envelope_vector" in vectors:
        vec = vectors["sealed_envelope_vector"]
        envelope = vec.get("sealed_envelope_json")
        if _contains_placeholder(envelope):
            rep.pend("sealed_envelope_vector: awaiting reference constants")
        else:
            actual = hv.verify_sealed_plat(envelope)
            expected = vec.get("expected_outcome")
            if actual != expected:
                rep.fail("sealed_envelope_vector: outcome mismatch", expected, actual)
            else:
                rep.ok()


# ── Crown-jewel corpus ──────────────────────────────────────────────────────
def run_action_receipts(crown: dict, rep: Report) -> None:
    block = crown.get("action_receipt_v2")
    if not block:
        return
    for case in block.get("cases", []):
        cid = case["id"]
        expected = case["expected_outcome"]
        actual = hv.verify_action_receipt(case["receipt_json"])
        if actual != expected:
            rep.fail(f"action_receipt_v2/{cid}: outcome mismatch", expected, actual)
            continue
        rep.ok()


